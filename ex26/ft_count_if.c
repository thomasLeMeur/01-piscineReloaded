int	ft_count_if(char **tab, int (*f)(char *))
{
	int	i;
	int	nb;

	i = -1;
	nb = 0;
	if (tab)
		while (tab[++i])
			if ((*f)(tab[i]) == 1)
				nb++;
	return (nb);
}
