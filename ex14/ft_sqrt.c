int	ft_sqrt(int nb)
{
	int	div;

	if (nb < 0)
		return (0);
	else if (nb == 1)
		return (1);
	div = 2;
	while ((long)div * div < nb)
	{
		div++;
	}
	if (div * div == nb)
		return (div);
	else
		return (0);
}
